#include <REGX52.H>

// 定时器0初始化函数
void Timer0_Init() {
    TMOD &= 0xF0;  // 低四位清零，高四位不变
    TMOD |= 0x01;  // 低四位置一，高四位不变
    TL0 = 0x17;    // 设置定时初始值，定时1ms
    TH0 = 0xFC;    // 设置定时初始值，定时1ms
    TF0 = 0;       // 清除TF0标志
    TR0 = 1;       // 定时器0开始计时
    ET0 = 1;       // 使能定时器0中断
    EA = 1;        // 全局中断使能
    PT0 = 0;       // 设置定时器0中断优先级
}
